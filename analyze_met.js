async function run() {
  const classifications = {};
  const objectNames = {};
  
  for(let i=0; i<ids.length; i++) {
    try {
      const res = await fetch(`https://collectionapi.metmuseum.org/public/collection/v1/objects/${ids[i]}`);
      const data = await res.json();
      
      const c = data.classification || 'Unknown';
      const n = data.objectName || 'Unknown';
      
      classifications[c] = (classifications[c] || 0) + 1;
      objectNames[n] = (objectNames[n] || 0) + 1;
    } catch(e) {}
  }
  
  console.log("CLASSIFICATIONS:");
  console.dir(classifications);
  console.log("OBJECT NAMES:");
  console.dir(objectNames);
}
